#include "../so_long.h"

void  ft_ber(char *a )
{
	int i = 0;
	int j = 0;
	char str[] = ".ber";
	while (a[i])
		i++;
	while (str[j])
		j++;
	i -= 1;
	j -= 1;
	while (j > 0)
	{
		if (str[j] == a[i])
		{
			i--;
			j--;
		}
		else
		{
			ft_error();
		}
	}
}